public class EstadoRespuesta {
	private String status;

	/**
	 * @param status
	 */
	public EstadoRespuesta(String status) {
		super();
		this.status = status;
	}

	/**
	 * @return the status
	 */
	public String getStatus() {
		return status;
	}

	/**
	 * @param status the status to set
	 */
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
